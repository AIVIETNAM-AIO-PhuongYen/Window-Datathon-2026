def build_features(dates_input) -> pd.DataFrame:
    """
    Tạo calendar-only features từ cột Date.
    Có thể tính sẵn cho cả train lẫn test ngay từ đầu.
    """
    if isinstance(dates_input, pd.Series):
        dates = pd.DatetimeIndex(dates_input.values)
    else:
        dates = pd.DatetimeIndex(dates_input)

    df = pd.DataFrame({'Date': dates})
    d  = df['Date']

    # ── (a) Calendar cơ bản ──
    df['year']       = d.dt.year
    df['month']      = d.dt.month
    df['day']        = d.dt.day
    df['dow']        = d.dt.dayofweek       # 0=Thứ2, 6=CN
    df['doy']        = d.dt.dayofyear
    df['quarter']    = d.dt.quarter
    df['is_weekend'] = (df['dow'] >= 5).astype(int)
    df['dim']        = d.dt.days_in_month   # số ngày trong tháng

    df['days_to_eom']   = df['dim'] - df['day']    # còn bao nhiêu ngày đến cuối tháng
    df['days_from_som'] = df['day'] - 1             # đã qua bao nhiêu ngày từ đầu tháng

    # ── (b) Edge-of-month — KEY INSIGHT ──
    # Ngày 28-31 cuối tháng cao gấp đôi ngày giữa tháng!
    # Ngày 1-3 đầu tháng cũng cao (pattern lương)
    for k in [1, 2, 3]:
        df[f'is_last{k}']  = (df['days_to_eom'] <= k - 1).astype(int)
        df[f'is_first{k}'] = (df['days_from_som'] <= k - 1).astype(int)

    # ── (c) Trend + Regime ──
    # 3 regime rõ ràng: pre-2019 (cao), 2019 (transition), post-2019 (thấp hơn)
    df['t_days']          = (d - pd.Timestamp('2020-01-01')).dt.days
    df['t_years']         = df['t_days'] / 365.25
    df['regime_pre2019']  = (df['year'] <= 2018).astype(int)
    df['regime_2019']     = (df['year'] == 2019).astype(int)
    df['regime_post2019'] = (df['year'] >= 2020).astype(int)

    # ── (d) Fourier Seasonality ──
    # Biến mùa vụ thành sóng sin/cos → Ridge học được seasonality
    # Annual: 5 harmonics (bắt được pattern năm phức tạp)
    # Weekly: 2 harmonics
    # Monthly: 2 harmonics (bắt được pattern trong tháng)
    TAU = 2 * np.pi
    for k in range(1, 6):
        df[f'sin_y{k}'] = np.sin(TAU * k * df['doy'] / 365.25)
        df[f'cos_y{k}'] = np.cos(TAU * k * df['doy'] / 365.25)
    for k in range(1, 3):
        df[f'sin_w{k}'] = np.sin(TAU * k * df['dow'] / 7.0)
        df[f'cos_w{k}'] = np.cos(TAU * k * df['dow'] / 7.0)
    for k in range(1, 3):
        df[f'sin_m{k}'] = np.sin(TAU * k * (df['day'] - 1) / df['dim'])
        df[f'cos_m{k}'] = np.cos(TAU * k * (df['day'] - 1) / df['dim'])

    # ── (e) Ngày lễ cố định Việt Nam ──
    for (m, dd, name) in VN_FIXED_HOLIDAYS:
        df[f'hol_{name}'] = ((df['month'] == m) & (df['day'] == dd)).astype(int)

    # ── (f) Tết Âm lịch — dùng khoảng cách thay vì flag cố định ──
    # Tết rơi khác ngày mỗi năm → flag tháng 1-2 không bắt được chính xác
    tet_lut = {y: pd.Timestamp(v) for y, v in TET_DATES.items()}

    def nearest_tet_diff(dd):
        cands = [tet_lut.get(dd.year),
                 tet_lut.get(dd.year - 1),
                 tet_lut.get(dd.year + 1)]
        valid = [(dd - c).days for c in cands
                 if c is not None and abs((dd - c).days) <= 60]
        if not valid:
            return 999
        return min(valid, key=abs)

    diffs = np.array([nearest_tet_diff(dd) for dd in d])
    df['tet_days_diff'] = diffs                                          # -60 đến +60
    df['tet_in_7']      = (np.abs(diffs) <= 7).astype(int)
    df['tet_in_14']     = (np.abs(diffs) <= 14).astype(int)
    df['tet_before_7']  = ((diffs >= -7) & (diffs < 0)).astype(int)    # 7 ngày trước Tết
    df['tet_after_7']   = ((diffs > 0) & (diffs <= 7)).astype(int)     # 7 ngày sau Tết
    df['tet_on']        = (diffs == 0).astype(int)

    # ── (g) Black Friday thực sự (thứ Sáu cuối tháng 11) ──
    def is_black_friday(dd):
        if dd.month != 11:
            return 0
        last = pd.Timestamp(year=dd.year, month=11, day=30)
        last_fri = last - pd.Timedelta(days=(last.dayofweek - 4) % 7)
        return int(dd == last_fri)

    df['hol_black_friday'] = [is_black_friday(dd) for dd in d]

    # ── (h) Promo windows ──
    # Mỗi chiến dịch: flag đang trong promo, đã chạy bao lâu, còn bao lâu
    yrs = sorted(set(df['year'].tolist()))
    for (name, sm, sd, dur, disc, recur) in PROMO_SCHEDULE:
        in_prom = np.zeros(len(df), dtype=int)
        since   = np.full(len(df), -1.0)
        until   = np.full(len(df), -1.0)
        for y in range(min(yrs) - 1, max(yrs) + 2):
            if recur == 'odd' and y % 2 == 0:
                continue
            try:
                start = pd.Timestamp(year=y, month=sm, day=sd)
            except Exception:
                continue
            end  = start + pd.Timedelta(days=dur)
            mask = (d >= start) & (d <= end)
            mask_arr = mask.values
            in_prom[mask_arr] = 1
            since[mask_arr]   = (d[mask] - start).dt.days.values
            until[mask_arr]   = (end - d[mask]).dt.days.values
        df[f'promo_{name}']       = in_prom
        df[f'promo_{name}_since'] = since
        df[f'promo_{name}_until'] = until

    # ── (i) Năm lẻ / năm chẵn ──
    # Năm lẻ có Urban Blowout → Q3 biến động khác hẳn
    # 2023=lẻ, 2024=chẵn → quan trọng cho test
    df['is_odd_year'] = (df['year'] % 2).astype(int)

    return df

