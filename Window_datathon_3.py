def train_ridge(X, y, alpha=3.0):
    """Train Ridge trên log-space với z-score normalization"""
    mu    = X.mean(axis=0)
    sigma = X.std(axis=0)
    sigma[sigma == 0] = 1.0   # tránh chia 0
    Xs    = (X - mu) / sigma
    model = Ridge(alpha=alpha, random_state=42)
    model.fit(Xs, y)
    return model, mu, sigma

def predict_ridge(model, mu, sigma, X):
    return model.predict((X - mu) / sigma)

