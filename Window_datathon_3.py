def train_lgb_two_stage(X_tr, y_tr, w_tr, X_val, y_val, params, label=""):
    """
    2 giai đoạn:
    1. Early stopping để tìm best_iteration
    2. Retrain toàn bộ với best_iteration
    """
    # Giai đoạn 1: early stopping
    bo_es = lgb.train(
        params,
        lgb.Dataset(X_tr, y_tr, weight=w_tr),
        num_boost_round=5000,
        valid_sets=[lgb.Dataset(X_val, y_val)],
        callbacks=[
            lgb.early_stopping(300, verbose=False),
            lgb.log_evaluation(0)
        ]
    )
    best_iter = bo_es.best_iteration
    print(f"  {label} best_iteration: {best_iter}")

    # Giai đoạn 2: retrain toàn bộ
    X_full = np.vstack([X_tr, X_val])
    y_full = np.concatenate([y_tr, y_val])
    w_full = np.concatenate([w_tr, w_tr[-len(X_val):]])  # dùng weight tương tự cho val
    bo_final = lgb.train(
        params,
        lgb.Dataset(X_full, y_full, weight=w_full),
        num_boost_round=best_iter
    )
    return bo_final, best_iter

